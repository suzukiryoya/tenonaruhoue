	void TitleStage::CreateBGM() {
		App::GetApp()->GetScene<Scene>()->PlayBGM(L"titleBGM_Electronic_Circuit.wav", 0.1f);
	}

	void TitleStage::OnCreate() {
		try {
			//ビューとライトの作成
			CreateViewLight();
			CreateBGM();
			CreateUI();
			CreateBackGround();
		}
		catch (...) {
			throw;
		}
	}

